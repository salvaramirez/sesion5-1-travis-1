<?php
class Calculator
{

    public function add($a, $b)
    {
        return $a + $b;
    }

    public function multiply($a, $b)
    {
        return $a * $b;
    }

    public function division($a, $b)
    {
        return $a / $b;
    }
    public function esPar($a)
    {
        return $a % 2;
    }
    public function esImpar($a)
    {
        return $a % 2;
    }


}
